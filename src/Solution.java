public class Solution {
    public static void main(String[] args) {



    }

    public boolean sleepIn(boolean weekday, boolean vacation) {
        if (weekday && !vacation)
            return false;
        else
            return true;
    }

    public int sumDouble(int a, int b) {
        if (a == b)
            return a*4;
        else
            return a + b;
    }







}
